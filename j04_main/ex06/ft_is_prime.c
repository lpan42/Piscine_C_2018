#include<stdio.h>

int ft_is_prime(int nb)
{
	int i;
	
	i = 2;
	if (nb <= 1)
		return (0);
	if (nb > 1)
		while (i * i <= nb)
		{	
			if (nb % i == 0)
				return (0);
			i++;
		}
	return (1);
}

int main()
{
	printf("%d\n", ft_is_prime(89));
	return (0);
}

